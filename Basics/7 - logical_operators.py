price = 25
x = price > 10 and price < 30 # AND - returns 'True' - True only, if both statements are true
x = price > 10 or price < 30 # OR - returns 'True' - True only, at least one statement is true
x = not price > 10 # NOT - returns 'True' - True only if none of the statement is true

